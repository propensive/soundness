package galilei

import vacuous.*

// An entry's metadata, as one value: its type, size in bytes, and millisecond-epoch timestamps.
// `created` is `Unset` on filesystems that do not record creation times (most POSIX filesystems,
// and WASI).
case class Stat
  ( entry:    Entry,
    size:     Long,
    modified: Long,
    accessed: Long,
    created:  Optional[Long] )
