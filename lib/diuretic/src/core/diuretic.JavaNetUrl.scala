package diuretic

import anticipation.*

import java.net as jn

object JavaNetUrl extends Abstractable, Instantiable:
  type Self = jn.URL
  type Result = Text
  type Source = Text
  type Domain = Urls

  def genericize(value: jn.URL): Text = value.toString.tt
  def apply(text: Text): jn.URL = jn.URI(text.s).nn.toURL().nn
