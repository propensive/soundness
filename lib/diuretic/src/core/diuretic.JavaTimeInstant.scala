package diuretic

import anticipation.*

import java.time as jt

object JavaTimeInstant extends Abstractable, Instantiable:
  type Self = jt.Instant
  type Source = Long
  type Result = Long
  type Domain = Instants

  def genericize(value: jt.Instant): Long = value.toEpochMilli
  def apply(long: Long): jt.Instant = jt.Instant.ofEpochMilli(long).nn
