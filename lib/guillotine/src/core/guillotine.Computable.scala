package guillotine

import language.experimental.pureFunctions

import java.io as ji

import scala.jdk.StreamConverters.StreamHasToScala

import anticipation.*
import contingency.*
import gossamer.*
import prepositional.*
import proscenium.*
import rudiments.*
import spectacular.*
import turbulence.*

object Computable:
  given stream: Stream[Text] is Computable = proc =>
    val reader = ji.BufferedReader(ji.InputStreamReader(proc.getInputStream))
    reader.lines().nn.toScala(Stream).map(_.tt)

  given list: List[Text] is Computable = proc =>
    val reader = ji.BufferedReader(ji.InputStreamReader(proc.getInputStream))
    reader.lines().nn.toScala(List).map(_.tt)

  given text: Text is Computable = proc =>
    Text.construct(stream.compute(proc).map(_.s).each(append(_)))

  given string: String is Computable = proc =>
    Text.construct(stream.compute(proc).map(_.s).each(append(_))).s

  given dataStream: Tactic[StreamError] => Stream[Bytes] is Computable =
    proc => Readable.inputStream.stream(proc.getInputStream.nn)

  given exitStatus: Exit is Computable = _.waitFor() match
    case 0     => Exit.Ok
    case other => Exit.Fail(other)

  given unit: Unit is Computable = exitStatus.map(_ => ())

  given instantiable: [instantiable: Instantiable across Paths from Text]
        =>  instantiable is Computable =
    path => instantiable(text.compute(path))

trait Computable:
  type Self
  def compute(process: java.lang.Process): Self

  def map[self2](lambda: Self => self2): self2 is Computable =
    process => lambda(compute(process))
