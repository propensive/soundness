package adversaria

import soundness.*

final case class ident() extends StaticAnnotation
final case class unique() extends StaticAnnotation
final case class number(number: Int) extends StaticAnnotation
final case class ref(x: Int) extends StaticAnnotation

// A type-parameterized annotation, used to check that adversaria can read and
// filter annotations by their type argument (e.g. `@marker[Person]`).
final case class marker[topic](id: Int) extends StaticAnnotation

case class Marked
  ( @marker[Person](1) @marker[Company](2) one: Int,
    @marker[Company](3)                    two: Int )

// A bare `@marker(вҖҰ)` (no explicit type argument) infers `marker[Any]`, so it is
// read by a `marker[Any]` query alongside an explicit `@marker[Any]`, but not by
// a `marker[Person]` query.
case class Tagged
  ( @marker(1)         bare:     Int,
    @marker[Any](2)    anyArg:   Int,
    @marker[Person](3) specific: Int )

case class Person(name: Text, @ident email: Text)

@number(10)
case class Company(name: Text)

case class Employee(person: Person, @ident @unique code: Long)
case class Letters(@ref(1) alpha: Int, @ref(2) @ref(3) beta: Int, gamma: Int, @ref(4) delta: Int)

object Example1:
  val foo: Int = 42
  val bar: String = "BAR"
  val baz: 12 = 12

sealed trait Colored

@number(3)
case class Hsv(hue: Double, saturation: Double, @ident value: Double) extends Colored

@unique()
case class Rgb(red: Int, green: Int, blue: Int) extends Colored
