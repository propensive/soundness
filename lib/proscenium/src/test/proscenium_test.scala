package proscenium

import soundness.*

object Tests extends Suite(m"Proscenium Tests"):
  def run(): Unit =
    // Laziness probes for the opaque `Progression` (Phase 8 acceptance guards): the cons keeps
    // its tail unforced, pattern-matching forces only the head, and `take`/`continually` never
    // force beyond the demanded prefix (an eager implementation would throw or diverge here).
    test(m"Progression cons does not force its tail"):
      var forced = 0
      def rest: Progression[Int] = { forced += 1; Progression() }
      val stream: Progression[Int] = 1 #:: rest
      forced

    . assert(_ == 0)

    test(m"Progression pattern match forces only the head"):
      var forced = 0
      def rest: Progression[Int] = { forced += 1; Progression() }
      val stream: Progression[Int] = 42 #:: rest
      val head = stream match { case head #:: _ => head }
      (head, forced)

    . assert(_ == (42, 0))

    test(m"take on an infinite stream does not diverge"):
      Progression.continually(7).take(3).toList

    . assert(_ == List(7, 7, 7))

    test(m"#::: keeps its lazy suffix unforced"):
      var forced = 0
      def suffix: Progression[Int] = { forced += 1; Progression(9) }
      val stream: Progression[Int] = Progression(1, 2) #::: suffix
      forced

    . assert(_ == 0)
