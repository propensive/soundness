package cordillera

import vacuous.*

object FrameType:
  def fromId(id: Int): Optional[FrameType] = id match
    case 0x0 => Data
    case 0x1 => Headers
    case 0x3 => RstStream
    case 0x4 => Settings
    case 0x6 => Ping
    case 0x7 => GoAway
    case 0x8 => WindowUpdate
    case 0x9 => Continuation
    case _   => Unset

// The HTTP/2 frame types this client handles (RFC 7540 §6). PushPromise (5) is
// not supported (the client disables server push) and Priority (2) is parsed only
// to be ignored, so neither has a dedicated decode path.
enum FrameType:
  case Data, Headers, RstStream, Settings, Ping, GoAway, WindowUpdate, Continuation

  def id: Int = this match
    case Data         => 0x0
    case Headers      => 0x1
    case RstStream    => 0x3
    case Settings     => 0x4
    case Ping         => 0x6
    case GoAway       => 0x7
    case WindowUpdate => 0x8
    case Continuation => 0x9
