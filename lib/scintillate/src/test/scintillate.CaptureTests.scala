package scintillate

import soundness.*

import strategies.throwUnsafely
import logging.silentLogging

// A handler receives one live exchange as an `HttpConnection` capability; capture checking
// prevents the connection (or its respond sink) being retained past the handler invocation.
object CaptureTests extends Suite(m"Connection confinement tests"):
  def run(): Unit =
    test(m"the connection cannot be stashed in an outer variable"):
      demilitarize:
        def attempt(server: SocketServer)
          ( using Monitor, Probate, (HttpServerEvent is Loggable)^, Tactic[ServerError] )
        :   Unit =
          var stash: () => Unit = () => ()

          server.handle:
            stash = () => summon[HttpConnection].respond(Http.Response(Http.Ok)(t""))
            Http.Response(Http.Ok)(t"")

          ()
    . assert(_.nonEmpty)

    // A streamed response body may retain the connection it answers (`Http.Response^
    // {connection}`) but nothing else scoped: a body reading a *foreign* capability
    // would outlive the handler through a stream the server never framed.
    test(m"a response body may not capture a foreign stream"):
      demilitarize:
        def attempt(server: SocketServer, foreign: (Stream[Data] over Credit)^)
          ( using Monitor, Probate, (HttpServerEvent is Loggable)^, Tactic[ServerError] )
        :   Unit =
          server.handle:
            Http.Response(Http.Ok)(Http.Body.Flowing(() => foreign))

          ()
    . assert(_.nonEmpty)
