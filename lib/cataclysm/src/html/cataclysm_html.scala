package cataclysm

import scala.util.NotGiven

import anticipation.*
import honeycomb.*
import prepositional.*

// Expands to the attribute (`"class"` or `"id"`) that `name` denotes in the
// in-scope `Styles` stylesheet, failing to compile if it denotes neither (or
// both).
transparent inline def attributeFor[name <: Label]: Text =
  ${cataclysm.protointernal.attributeFor[name]}

// Lets a `Css.Style` be used as an inline `style="вҖҰ"` attribute value in
// Honeycomb, e.g. `Div(style = Css.Style(color = rgb, width = 4.0*Px))`.
given inlineStyle: (Css.Style is Attributive to Whatwg.Css) = _ -> _.text

// Import this to make `Tag.foo(вҖҰ)` check `foo` against the in-scope `Styles`
// stylesheet, attaching `class="foo"` or `id="foo"` accordingly.
package cssBindings:
  inline given checkedBinding: [name <: Label] => NotGiven[name =:= "apply"]
  =>  Attribution of name =
    Attribution(attributeFor[name]).asInstanceOf[Attribution of name]
