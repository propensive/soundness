package coaxial

import soundness.*

import strategies.throwUnsafely
import charEncoders.utf8
import inimitable.*
import jsonPrinters.minimal
import threading.platform

object Tests extends Suite(m"Coaxial tests"):
  def run(): Unit = unsafely:

    val u = udp"3876".json.show
    u.decode[Json].as[UdpPort]

    supervise:
      val task = async:
        val udpServer: Unit = remote.dispatch:
          ' {
              unsafely:
                supervise:
                  val promise: Promise[Text] = Promise()

                  val server = ${port.put}.listen[Text]: in =>
                    UdpResponse.Reply
                      ( jvmInstanceId.show.sysData).also(promise.fulfill(in.data.utf8) )

                  promise.await()
            }

        test(m"Test UDP server"):
          udpServer(udp"3962")
        . assert()


      test(m"Send UDP messages until port opens"):
        Thread.sleep(5000)
        udp"3962".transmit(jvmInstanceId.show)
      . assert()

      task.await()
