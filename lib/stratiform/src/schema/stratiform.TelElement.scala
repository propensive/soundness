package stratiform

import anticipation.*
import vacuous.*

// Semantic model from §18.2 of the TEL specification. The result of
// applying type assignment (§20.2) to the presentation model: every
// atom and compound is replaced by an Element with a schema-derived
// Type.
//
// An Element is either a Node (Struct- or Flag-typed) or a Value
// (Scalar-typed leaf). `keywordIndex` records the position of the
// element's keyword in the parent's keyword order — sufficient to
// recover the keyword string from the schema. The document root's
// Node has `keywordIndex = Unset`.

sealed trait TelElement

object TelElement:
  case class Node
     ( keywordIndex: Optional[Int],
       elementType:  TelSchema.Type,
       children:     IArray[TelElement] )
  extends TelElement

  case class Value
     ( keywordIndex: Int,
       scalarType:   TelSchema.Scalar,
       text:         Text )
  extends TelElement
