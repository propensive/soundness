package parasite

import language.experimental.pureFunctions

import fulminate.*

// Contains *thrown* exceptions escaping fire-and-forget workers spawned within its region — a
// catch-all distinct from the typed `contingency.trap`, which handles declared *emitted* errors. It
// holds the error-trap `probate` policy and, in `protect`, runs the body under a child supervision
// `Subscope` carrying that policy: workers spawned within `protect` capture the subscope as their
// parent and so reach this `probate`. Child-fate (`cleanup`) is delegated unchanged to the enclosing
// scope's probate; only the failure branch (`trap`) is overridden. A handled error whose remedy is
// `Reject` — like one the handler does not match — bubbles to the enclosing scope, so containments
// compose as they nest.
class Containment(handler: PartialFunction[Error, Remedy], parent: Monitor^)
extends caps.ExclusiveCapability:
  val probate: Probate^ = new Probate:
    def cleanup(worker: Worker): Unit = parent.probate.cleanup(worker)

    override def trap(worker: Worker, error: Error): Remedy =
      if handler.isDefinedAt(error) then handler(error) match
        case Remedy.Accept          => Remedy.Accept
        case Remedy.Reject          => parent.probate.trap(worker, error)
        case Remedy.Escalate(other) => parent.probate.trap(worker, other)
      else parent.probate.trap(worker, error)

  // Runs `body` under a child supervision scope governed by this containment; daemons and abandoned
  // tasks spawned within it route their escaped exceptions here.
  def protect[result](body: Monitor ?=> result): result = body(using Subscope(parent, probate))
