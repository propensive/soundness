package hellenism

import java.net as jn
import java.io as ji

import anticipation.*
import gossamer.*
import prepositional.*
import vacuous.*

sealed trait ClasspathEntry:
  def javaUrl: jn.URL = this match
    case ClasspathEntry.Directory(path) => ji.File(path.s).toURI.nn.toURL.nn
    case ClasspathEntry.Jar(path)       => ji.File(path.s).toURI.nn.toURL.nn
    case ClasspathEntry.Url(url)        => jn.URL(url.s)
    case ClasspathEntry.JavaRuntime     => jn.URL("jrt:/")

object ClasspathEntry:
  case class Directory(path: Text) extends ClasspathEntry:
    def apply[directory: Instantiable across Paths from Text](): directory = directory(path)

  case class Jar(path: Text) extends ClasspathEntry:
    def apply[file: Instantiable across Paths from Text](): file = file(path)

  case class Url(url: Text) extends ClasspathEntry:
    def apply[instantiable: Instantiable across Urls from Text](): instantiable = instantiable(url)

  case object JavaRuntime extends ClasspathEntry

  def apply(url: jn.URL): Optional[ClasspathEntry] = url.getProtocol.nn.tt match
    case t"jrt" =>
      ClasspathEntry.JavaRuntime

    case t"file" =>
      val path: Text = url.getPath.nn.tt
      if path.ends(t"/") then ClasspathEntry.Directory(path) else ClasspathEntry.Jar(path)

    case t"http" | t"https" =>
      ClasspathEntry.Url(url.toString.tt)

    case _ =>
      Unset
