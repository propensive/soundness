package plutocrat

import anticipation.*
import fulminate.*

object IsinError:
  enum Reason:
    case InvalidCharacter(index: Int, char: Char)
    case BadCountryCode(code: Text)
    case WrongLength(length: Int)
    case LuhnCheck

  export Reason.*

  given communicable: Reason is Communicable =
    case BadCountryCode(code) => m"its country code $code was not valid"
    case WrongLength(length)  => m"it had length $length, but it should be 12 characters long"
    case LuhnCheck            => m"its last digit failed the Luhn check"

    case InvalidCharacter(index, char) =>
      m"the character $char at position $index is not a digit or uppercase letter"

case class IsinError(reason: IsinError.Reason)(using Diagnostics)
extends Error(m"the ISIN number is not valid because $reason")
