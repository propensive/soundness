package probably

import anticipation.*
import rudiments.*
import digression.*
import gossamer.*

object Juncture:
  given ordering: Ordering[Juncture] = Ordering.by[Juncture, Int](_.start).orElseBy(-_.end)

case class Juncture
  ( id:         Int,
    path:       Text,
    className:  Text,
    methodName: Text,
    start:      Int,
    end:        Int,
    lineNo:     Int,
    symbolName: Text,
    treeName:   Text,
    branch:     Boolean,
    ignored:    Boolean,
    code:       List[Text] ):

  def contains(right: Juncture): Boolean =
    (right.start >= start && right.end <= end && !(right.start == start && right.end == end))
    || treeName == t"DefDef" && right.treeName != t"DefDef" && className == right.className
    && methodName == right.methodName

  def shortCode: Text =
    val lines = code.flatMap(_.cut(t"\\n"))
    if lines.length > 1 then t"${lines.head}..." else lines.head

  def method: StackTrace.Method =
    StackTrace.Method
      ( StackTrace.rewrite(className.s),
        StackTrace.rewrite(methodName.s, method = true) )
