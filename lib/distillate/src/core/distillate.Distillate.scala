package distillate

import scala.quoted.*

import anticipation.*
import fulminate.*
import proscenium.*

object Distillate:
  private given realm: Realm = realm"distillate"
  def enumerable[enumeration <: reflect.Enum: Type]: Macro[enumeration is Enumerable] =
    import quotes.reflect.*

    val companion = Ref(TypeRepr.of[enumeration].typeSymbol.companionModule).asExpr

    '{
        new Enumerable:
          type Self = enumeration
          val name: Text = ${Expr(TypeRepr.of[enumeration].show)}.tt
          val values: IArray[enumeration] =
            $ {
                companion.absolve match
                  case '{$companion: companion} =>
                    val ref = TypeRepr.of[companion].typeSymbol.declaredMethod("values")
                              . headOption
                              . getOrElse:
                                  halt:
                                    m"enum ${TypeRepr.of[enumeration].show} is not a simple choice"

                    companion.asTerm.select(ref).asExprOf[Array[enumeration]]
              }
            . asInstanceOf[IArray[enumeration]]  }
