package rudiments

import java.util.concurrent.locks as juclo

class Mutex():
  private val lock: juclo.ReentrantLock = juclo.ReentrantLock()

  inline def apply[result](inline block: => result): result =
    lock.lock()
    try block finally lock.unlock()
