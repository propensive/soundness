package rudiments

object Exit:
  def apply(value: Int): Exit = if value == 0 then Ok else Fail(value)

enum Exit:
  case Ok
  case Fail(status: Int)

  def apply(): Int = this match
    case Ok           => 0
    case Fail(status) => status

  def terminate(): Nothing =
    System.exit(apply())
