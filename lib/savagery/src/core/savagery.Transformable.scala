package savagery

import geodesy.*
import prepositional.*
import vacuous.*


object Transformable:
  def apply[value]
    ( get: value => List[Transform], put: (value, List[Transform]) => value )
  :   value is Transformable =

    new Transformable:
      type Self = value
      def transforms(self: value): List[Transform] = get(self)
      def withTransforms(self: value, transforms: List[Transform]): value = put(self, transforms)


  given rectangle: Rectangle is Transformable =
    Transformable(_.transforms, (figure, ts) => figure.copy(transforms = ts))

  given outline: Outline is Transformable =
    Transformable(_.transforms, (figure, ts) => figure.copy(transforms = ts))

  given ellipse: Ellipse is Transformable =
    Transformable(_.transforms, (figure, ts) => figure.copy(transforms = ts))

  given svg: Svg is Transformable =
    Transformable(_.transforms, (figure, ts) => figure.copy(transforms = ts))



trait Transformable extends Typeclass:
  def transforms(self: Self): List[Transform]
  def withTransforms(self: Self, transforms: List[Transform]): Self
