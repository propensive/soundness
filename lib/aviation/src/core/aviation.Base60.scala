package aviation

import proscenium.*

object Base60:
  def apply(int: Int): Base60 =
    (((int%60) + 60)%60).absolve match
      case value: Base60 => value

  def unapply(value: Int): Option[Base60] =
    if value < 0 || value > 59 then None else Some(value.asInstanceOf[Base60])

