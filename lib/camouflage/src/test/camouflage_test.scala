package camouflage

import anticipation.*
import fulminate.*
import gossamer.*
import probably.*

object Tests extends Suite(m"Camouflage tests"):
  def run(): Unit =
    test(m"Check first entry is removed"):
      val cache = LruCache[Int, Text](4)
      cache(1)(t"one") // should be evicted
      cache(2)(t"two")
      cache(3)(t"three")
      cache(4)(t"four")
      cache(5)(t"five")
      cache(1)(t"ein")
    .assert(_ == t"ein")

    test(m"Check that an access stops an element being evicted"):
      val cache = LruCache[Int, Text](4)
      cache(1)(t"one")
      cache(2)(t"two")
      cache(3)(t"three")
      cache(4)(t"four")
      cache(1)(t"ein")
      cache(5)(t"five")
      cache(1)(t"un")
    .assert(_ == t"one")

    test(m"LRU eviction after series of accesses"):
      val cache = LruCache[Int, Text](4)
      cache(1)(t"one")
      cache(2)(t"two")
      cache(3)(t"three")
      cache(4)(t"four")
      cache(1)(t"ein")
      cache(2)(t"zwei")
      cache(3)(t"drei")
      cache(5)(t"five")
      cache(4)(t"vier")
    .assert(_ == t"vier")
