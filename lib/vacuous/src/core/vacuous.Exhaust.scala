package vacuous

// A capability token gating operations that exhaust a lazy structure — computing the size of a
// `Progression`, sorting it, or converting it to a strict collection — which may diverge if the
// structure is unbounded. A real singleton for the same Scala.js reason as `Unsafe` and `Trek`;
// consuming positions take it `using erased`. (Not `Force`: quantitative already exports the
// physical unit `Force`, and vacuous is wildcard-imported nearly everywhere, where file-level
// imports shadow package-mates.)
object Exhaust extends Exhaust

sealed trait Exhaust
