package lithosphere

import scala.util.chaining.*

import dotty.tools.dotc.*, ast.*, core.*, Contexts.*, Decorators.*, plugins.*, ast.tpd.*, util.*

class Renumberer(options: List[String]) extends PluginPhase:
  val phaseName: String = "renumber"
  override val runsAfter: Set[String] = Set("pickler")
  override val runsBefore: Set[String] = Set("inlining")
  var previous = 0
  var src: Option[SourceFile] = None

  override def run(using Context): Unit =
    val unit = ctx.compilationUnit
    unit.tpdTree = mapper.transform(unit.tpdTree)

  private def mapper(using Context) = new TreeMap:
    override def transform(tree: Tree)(using context: Context): Tree =
      if Some(context.source) != src then
        src = Some(context.source)
        previous = 0

      super.transform(tree).tap: tree =>
        if tree.span.exists then
          println(tree.span.start - previous)
          previous = tree.span.start
