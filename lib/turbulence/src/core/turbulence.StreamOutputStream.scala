package turbulence

import java.io as ji

import scala.collection.mutable as scm

import anticipation.*
import denominative.*
import rudiments.*
import vacuous.*

class StreamOutputStream() extends ji.OutputStream:
  private val buffer: scm.ArrayBuffer[Byte] = scm.ArrayBuffer()
  private val chunks: Spool[Data] = Spool()

  def stream: LazyList[Data] = chunks.stream
  def write(int: Int): Unit = buffer.append(int.toByte)
  override def close(): Unit = flush().also(chunks.stop())

  override def write(bytes: Array[Byte] | Null): Unit =
    flush()
    if bytes != null then chunks.put(bytes.immutable(using Unsafe))

  override def write(bytes: Array[Byte] | Null, offset: Int, length: Int): Unit =
    flush()
    if bytes != null then chunks.put(bytes.slice(offset, offset + length).immutable(using Unsafe))

  override def flush(): Unit = if !buffer.nil then
    chunks.put(buffer.toArray.immutable(using Unsafe))
    buffer.clear()
