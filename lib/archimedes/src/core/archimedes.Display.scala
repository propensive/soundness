package archimedes

import anticipation.*
import gossamer.*
import prepositional.*

// The value of the `display` attribute on the root `<math>` element: `block`
// for displayed equations, `inline` for equations set in running text.

object Display:
  given encodable: Display is Encodable in Text =
    case Block  => t"block"
    case Inline => t"inline"

  def unapply(text: Text): Option[Display] = text match
    case t"block"  => Some(Block)
    case t"inline" => Some(Inline)
    case _         => None

enum Display:
  case Block, Inline
