package geodesy

import scala.math

import hypotenuse.*
import symbolism.*

export geodesy.internal.{Location, Angle}

val Degree = Angle(ПҖ/180.0)
val ArcMinute = Degree/60.0
val ArcSecond = ArcMinute/60.0

extension (double: Double)
  def rad: Angle = Angle(double)
  def deg: Angle = Degree*double

export CardinalWind.{North, South, East, West}
export IntercardinalWind.{Northeast, Southeast, Southwest, Northwest}

export HalfWind
. { NorthNortheast, EastNortheast, EastSoutheast, SouthSoutheast, SouthSouthwest, WestSouthwest,
    WestNorthwest, NorthNorthwest }

package compassBearings:
  given fourPointCompassBearing: Compass[4] is Directional = Compass[4](_)
  given eightPointCompassBearing: Compass[8] is Directional = Compass[8](_)
  given sixteenPointCompassBearing: Compass[16] is Directional = Compass[16](_)
  given fromNorthBearing: Angle is Directional = identity(_)
  given fromEastBearing: Angle is Directional = radians => radians - Angle(math.Pi/2)

type Compass[points <: 4 | 8 | 16] = points match
  case 4  => CardinalWind
  case 8  => CardinalWind | IntercardinalWind
  case 16 => CardinalWind | IntercardinalWind | HalfWind
