            val p1 = % / "foo" / "bar" / "quux" / "bippy" / "abc"
            val p2 = % / "foo" / "baz" / "quux" / "bop" / "def"
            p1.conjunction(p2)
          }

      bench(m"Find conjunction of 6-element paths")(target = 500*Milli(Second)):
        '{
            val p1 = % / "foo" / "bar" / "quux" / "bippy" / "abc" / "ghi"
