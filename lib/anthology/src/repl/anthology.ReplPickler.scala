package anthology

import scala.quoted.*
import scala.quoted.runtime.impl.QuotesImpl

import dotty.tools.dotc.ast.tpd
import dotty.tools.dotc.quoted.PickledQuotes

// Pickles the AST of an inline block to TASTy (a `List[String]`), so it can be
// carried as plain data to runtime and recompiled by `ReplModuleCompiler`. This
// reaches under the `Expr` surface API — which forbids lifting a built tree into
// a re-stageable quote — by pickling the tree to bytes directly.
object ReplPickler:
  inline def pickle(inline body: Any): List[String] = ${pickleMacro('body)}

  def pickleMacro(body: Expr[Any])(using Quotes): Expr[List[String]] =
    import quotes.reflect.*

    val context = quotes.asInstanceOf[QuotesImpl].ctx
    val tree = body.asTerm.asInstanceOf[tpd.Tree]
    val pickled: List[String] = PickledQuotes.pickleQuote(tree)(using context)

    Expr(pickled)
