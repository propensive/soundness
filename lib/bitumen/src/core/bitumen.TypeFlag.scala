package bitumen

import hieroglyph.*, charEncoders.asciiEncoder, textMetrics.uniformMetric
import hypotenuse.*, arithmeticOptions.overflow.unchecked

enum TypeFlag:
  case File
  case Link
  case Symlink
  case CharSpecial
  case BlockSpecial
  case Directory
  case Fifo
  case Contiguous
  case NextFile
  case GlobalExtension
  case LongLink
  case LongName
  case Sparse

  def id: Char = this match
    case File            => '0'
    case Link            => '1'
    case Symlink         => '2'
    case CharSpecial     => '3'
    case BlockSpecial    => '4'
    case Directory       => '5'
    case Fifo            => '6'
    case Contiguous      => '7'
    case NextFile        => 'x'
    case GlobalExtension => 'g'
    case LongLink        => 'K'
    case LongName        => 'L'
    case Sparse          => 'S'
