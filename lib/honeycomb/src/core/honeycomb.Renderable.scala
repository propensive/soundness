import anticipation.*
import digression.*
import doms.html.whatwg.*
import fulminate.*
import gossamer.*
import prepositional.*
import rudiments.{bind, flatMap, map}
import spectacular.*
import vacuous.*

object Renderable:
  given message: Message is Renderable:
    type Form = Phrasing

      def render(message: Message): Html of Phrasing =
        val elements: List[Html of Phrasing] = message.segments.bind:
          case message: Message => List[Html of Phrasing](render(message))
          case text: Text       => List[Html of Phrasing](text)
          case _                => List[Html of Phrasing]()

        Fragment(elements*)

  given stackTrace: StackTrace is Renderable in Flow = stackTrace =>
    given attribution: (Attribution of "at" | "class" | "stack" | "method" | "file" | "line") =
      Attribution.classes()

    val rows = stackTrace.frames.map: (frame: StackTrace.Frame) =>
      Tr
        ( Td.at(Code(t"at")),
          Td.`class`(Code(frame.method.className)),
          Td.method(Code(frame.method.method)),
          Td.file(Code(frame.file)),
          Td(Code(t":")),
          Td.line(Code(frame.line.let(_.show).or(t""))) )

    Div.stack
      ( H2(stackTrace.component),
        H3(stackTrace.className),
        H4(stackTrace.message.html),
        Table(Tbody(rows*)) )


trait Renderable extends Typeclass, Formal:
  def render(value: Self): Html of Form
