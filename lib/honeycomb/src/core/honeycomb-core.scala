package honeycomb

import scala.util.NotGiven

import anticipation.*
import contextual.*
import prepositional.*
import proscenium.*
import rudiments.*
import vacuous.*

import language.dynamics

extension [renderable: Renderable](value: renderable)
  def html: Html of renderable.Form = renderable.render(value)

package attributives:
  given textAttributes: [target] => Text is Attributive to target =
    (key, value) => (key, value)

extension (inline context: StringContext)
  transparent inline def h: Interpolation = interpolation[Html](context)

extension (html: Seq[Html])
  def nodes: IArray[Node] =
    var count = 0

    for item <- html do item match
      case fragment: Fragment => count += fragment.nodes.length
      case _                  => count += 1

    val array = new Array[Node](count)

    var index = 0
    for item <- html do item match
      case Fragment(nodes*) => for node <- nodes do
        array(index) = node
        index += 1

      case node: Node =>
        array(index) = node
        index += 1

    array.immutable(using Unsafe)

package doms.html:
  given whatwg: Whatwg = Whatwg()

package stylesheets:
  given uncheckedClasses: [classname <: Label: ValueOf] => NotGiven[classname =:= "apply"]
        => Stylesheet of classname =
    new Stylesheet(Set(valueOf[classname])):
      type Topic = classname
